#include <stdio.h>

int main() {
    double x = 1.0, y = 2.0, z;

    // บรรทัดที่ 1: x = 1.0, y = 2.0, z ยังไม่กำหนด
    printf("1: x = %.1f, y = %.1f, z = ไม่กำหนด\n", x, y);

    x = y + 5.0;  // บรรทัดที่ 2: x = 2.0 + 5.0 = 3.0
    printf("2: x = %.1f, y = %.1f, z = ไม่กำหนด\n", x, y);

    y = x / 2.0;  // บรรทัดที่ 3: y = 3.0 / 2.0 = 1.5
    printf("3: x = %.1f, y = %.1f, z = ไม่กำหนด\n", x, y);

    y = (x + 3.0) + 4.0;  // บรรทัดที่ 4: y = (3.0 + 3.0) + 4.0 = 10.0
    printf("4: x = %.1f, y = %.1f, z = ไม่กำหนด\n", x, y);

    x = -0.5 - y;  // บรรทัดที่ 5: x = -0.5 - 10.0 = -10.5
    printf("5: x = %.1f, y = %.1f, z = ไม่กำหนด\n", x, y);

    z = x + y;  // บรรทัดที่ 6: z = -10.5 + 10.0 = -0.5
    printf("6: x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0;
}